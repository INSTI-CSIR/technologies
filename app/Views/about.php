              <h4  class="section-title text-primary"id="paragraph">Institute For Scientific & Technological Information.</h4>

              <p>INSTI (Institute for Scientific & Technological Information) is one of thirteen institutes within the Council for Scientific and Industrial Research (CSIR). INSTI has five technical divisions, namely; Communications, Electronics, Fluid Science Division, Geospatial & Information Science and Printing & Publishing Divisions.</p>

              <h4 class="section-title text-primary"id="paragraph">Our Mandate</h4>

              <p>To develop a national capacity and capability for efficient and effective provision of scientific and technological information on demand, for the benefit of research scientists, policy makers and industrialists in appropriately packaged form for national development. </p>
              <h4 class="section-title text-primary"id="paragraph">Our Vision</h4>

              <p>To become a national leader in making accessible the world's intellectual output of scientific and technological knowledge as well as resource development maps to support Research and Development (R&D) activities in Ghana. </p>
              <h4 class="section-title text-primary"id="paragraph">CSIR-INSTI</h4>

              <p>The Institute for Scientific and Technological Information (INSTI) of the Council for Scientific and Industrial Research (CSIR) is the hub of integrated scientific and technical information (STI) in Ghana that provides the major public research and academic institutions as well as the industrial sector with resources and services designed to improve dissemination of, and access to indigenous and international STI. </p>

              <p>The Institute serves as the main STI resources clearinghouse for CSIR. INSTI’s mandate is to develop national capacity and capability for the efficient and effective delivery of real-time scientific and technological information (STI) and customized knowledge on demand for the benefit of policy makers, research scientists, industrialists, and others in appropriately packaged form for national development.</p>
              <h4 class="section-title text-primary"id="paragraph">CSIR</h4>

              <p>The CSIR was established by NLC Decree 293 of October 10, 1968, amended by NLCD 329 of 1969, and re-established in its present form by CSIR Act 521 on November 26, 1996. The genesis of the Council, however, dates back to the erstwhile National Research Council (NRC), which was established by the government of Ghana in August 1958 to organize and coordinate scientific research in Ghana. In 1963, the NRC merged with the former Ghana Academy of Sciences, a statutory learned society. Following a review in 1966, the Academy was reconstituted into, essentially, its original component bodies, namely a national research organization redesignated the CSIR and a learned Society, designated the Ghana Academy of Arts and Sciences.</span></p>

              <p>The distinctive features of the 1996 Act are the emphasis accorded private sector concerns and the introduction of market principles into the Council’s operations through the commercialization of research. In this connection, the Council is expected to generate part of its income through the sale of its products and services, and to institute a system of contract research.</p>

              <p>The CSIR consists of a Head Office and Thirteen (13) Research Institutes spread around the country with specific mandates to generate and apply innovative technologies, which efficiently and effectively exploit science and technology in its Research and Development (R&D) activities, which cover the broad areas of Agriculture, Industry, Agro-processing, Fisheries, Forestry, Water Resources, Building and Road Construction, Environment, Health, Natural and Social Sciences. </span></p>
              
             
 
 
                 
              
